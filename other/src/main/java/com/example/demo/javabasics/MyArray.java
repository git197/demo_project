package com.example.demo.javabasics;

public class MyArray {

    public static void main(String[] args) {
        //String 的length()是方法
        String ss="s";
        System.out.println(ss.length());
        //数组的length是属性
        String []sarr =new String[3];
        System.out.println(sarr.length);
    }
}
